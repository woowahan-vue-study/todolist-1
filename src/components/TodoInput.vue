<template>
  <input
    id="new-todo-title"
    class="new-todo"
    placeholder="할일을 추가해주세요"
    autofocus
    @keyup.enter="addTodo"
    v-model="inputValue"
  />
</template>

<script>
export default {
  name: "TodoInput",
  methods: {
    addTodo() {
      if (!this.inputValue) {
        return;
      }
      this.$store.dispatch("ADD_TODO", this.inputValue);
      this.inputValue = "";
    },
  },
  data() {
    return {
      inputValue: "",
    };
  },
};
</script>
